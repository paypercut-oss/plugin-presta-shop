// ===== GENERATOR =====

$outputDir = dirname(__DIR__) . '/translations';
if (!is_dir($outputDir)) {
    mkdir($outputDir, 0755, true);
}

$languages = array('bg', 'cs', 'da', 'de', 'en', 'es', 'fr', 'hu', 'it', 'nb', 'pl', 'ro', 'sv');

foreach ($languages as $lang) {
    $content = "<?php\n\n";
    $content .= "global \$_MODULE;\n";
    $content .= "\$_MODULE = array();\n";

    foreach ($strings as $source => $sourceStrings) {
        foreach ($sourceStrings as $str) {
            $key = '<{paypercut}prestashop>' . $source . '_' . md5($str);

            if ($lang === 'en') {
                $value = $str;
            } elseif (isset($translations[$lang][$str])) {
                $value = $translations[$lang][$str];
            } else {
                // Fallback to English if no translation
                $value = $str;
            }

            // Escape single quotes in value
            $value = str_replace("'", "\\'", $value);
            $content .= "\$_MODULE['" . $key . "'] = '" . $value . "';\n";
        }
    }

    $filePath = $outputDir . '/' . $lang . '.php';
    file_put_contents($filePath, $content);
    echo "Generated: {$filePath}\n";
}

echo "\nDone! Generated " . count($languages) . " translation files.\n";
